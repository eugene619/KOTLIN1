fun main() {
    //Arithmetic Operators
    var x = 20
    var y = 6

    println(x + y)
    println(x * y)
    println(x / y)
    println(x - y)
    println(x % y)  //Modulus

    //Comparison Operators
    var num1 = 45
    var num2 = 30

    println(num1 > num2)
    println(num1 < num2)
    println(num1 >= num2)
    println(num1 <= num2)
    println(num1 == num2)
    println(num1 != num2)

    //Assignment Operators
    var z = 56
    println(z)

    var number = 10
    number += 2
    println(number)

    var namba = 10
    namba -= 2
    println(namba)

    var no = 10
    no *= 2
    println(no)

    var num = 10
    num /= 2
    println(num)

    //Logic Operators- &&(and), ||(or), !(not)
    println(67 > 20 && 80 < 9)
    println(67 > 20 || 80 < 9)
    println(!(67 > 20 || 80 < 9))

}
